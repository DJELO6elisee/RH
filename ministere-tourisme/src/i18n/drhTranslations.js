// Fonction pour obtenir la traduction selon la langue
export const getDRHTranslation = (lang = 'fr', key) => {
  const keys = key.split('.');
  let translation = drhTranslations[lang] || drhTranslations.fr;
  
  for (const k of keys) {
    translation = translation?.[k];
    if (!translation) {
      // Fallback vers le français si la traduction n'existe pas
      translation = drhTranslations.fr;
      for (const k2 of keys) {
        translation = translation?.[k2];
      }
      break;
    }
  }
  
  return translation || key;
};

// Fonction pour traduire les catégories de la sidebar
export const translateCategory = (lang = 'fr', category) => {
  const categoryMap = {
    'PROFIL DE CARRIERE': 'sidebar.nomination',
    'Gestions des documents administratifs': 'sidebar.gestionsDocumentsAdministratifs',
    'Gestion du Personnel': 'sidebar.gestionPersonnel',
    'États et Rapports': 'sidebar.etatsRapports',
    'Identité': 'sidebar.identite',
    'ELEMENTS DU PROFIL DE CARRIERE': 'sidebar.carriere',
    'Organisation': 'sidebar.organisation',
    'Relations Sociales': 'sidebar.relationsSociales',
    'Paramètres': 'sidebar.parametres'
  };
  
  const key = categoryMap[category];
  if (key) {
    return getDRHTranslation(lang, key);
  }
  
  // Si pas de traduction spécifique, retourner le nom original
  return category;
};

